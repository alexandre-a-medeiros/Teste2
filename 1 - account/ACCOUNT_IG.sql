CREATE TABLE APP_STGOWN.ACCOUNT_PPZ_LOGIN_AUX_I(
   CLI_COD      NUMBER(10),
   CREATIONDATE DATE,
   PPZ_LOGIN    VARCHAR2(200))
   TABLESPACE TSD_MIGDW;

INSERT /*+ APPEND */INTO APP_STGOWN.ACCOUNT_PPZ_LOGIN_AUX_I
   SELECT
         CLI_COD,
         CREATIONDATE,
         PPZ_LOGIN
      FROM
         (SELECT /*+ PARALLEL(VCU,20) PARALLEL(CLG,20)*/
                CLG.CLG_CLICOD AS CLI_COD,
                CLG.CLG_CREATIONDATE AS CREATIONDATE,
                CLG.CLG_LOGIN || '@' || CLG.CLG_DOMAIN AS PPZ_LOGIN,
                DENSE_RANK() OVER (PARTITION BY CLG.CLG_CLICOD ORDER BY CLG.CLG_CREATIONDATE DESC,CLG.ROWID DESC) AS NRANK
             FROM
                APP_STGOWN.I_CRM_VELOXCUSTOMER VCU,
                APP_STGOWN.I_CRM_CUSTOMERLOGIN CLG
             WHERE
                CLG.CLG_CLICOD = VCU.VCU_CLICOD AND
                INSTR(CLG.CLG_DOMAIN,'.',1) = 0) 
      WHERE NRANK=1;

COMMIT;

CREATE UNIQUE INDEX APP_STGOWN.I_ACCOUNT_PPZ_LOGIN_AUX_I ON APP_STGOWN.ACCOUNT_PPZ_LOGIN_AUX_I(CLI_COD);

EXEC DBMS_STATS.GATHER_TABLE_STATS('APP_STGOWN','ACCOUNT_PPZ_LOGIN_AUX_I');

