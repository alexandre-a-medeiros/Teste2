DECLARE
	COUNT_TBL INTEGER;
	TBL_AUX VARCHAR2(30);
	QUERY	VARCHAR2(4000);
BEGIN
	
	/* PASSO 1: CRIAR TABELA AUXILIARE COM ACCOUNT IG CRM */
	
	TBL_AUX := 'ACCOUNT_AUX_I';
	
	SELECT COUNT(*) INTO COUNT_TBL FROM ALL_TABLES WHERE TABLE_NAME = TBL_AUX AND OWNER = 'APP_STGOWN';
	
	IF COUNT_TBL = 0 THEN
		EXECUTE IMMEDIATE 'CREATE TABLE APP_STGOWN.'||TBL_AUX||' NOLOGGING COMPRESS
		AS SELECT * FROM APP_RELOWN.ACCOUNT WHERE ROWNUM < 0';
	ELSE
		EXECUTE IMMEDIATE 'DROP TABLE APP_STGOWN.'|| TBL_AUX;
		EXECUTE IMMEDIATE 'CREATE TABLE APP_STGOWN.'||TBL_AUX||' NOLOGGING COMPRESS 
		AS SELECT * FROM APP_RELOWN.ACCOUNT WHERE ROWNUM < 0';
	END IF;
	
	EXECUTE IMMEDIATE 'ALTER SEQUENCE APP_RELOWN.SEQ_SRK_ACCOUNT INCREMENT BY 1';
		
END;
/

